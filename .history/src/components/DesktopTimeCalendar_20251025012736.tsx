// Â∞ÜÂçÅÂÖ≠ËøõÂà∂È¢úËâ≤ËΩ¨Êç¢‰∏∫ RGB
function hexToRgb(hex: string): { r: number; g: number; b: number } | null {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  return result
    ? {
        r: parseInt(result[1], 16),
        g: parseInt(result[2], 16),
        b: parseInt(result[3], 16)
      }
    : null;
}

// ËÆ°ÁÆóÁõ∏ÂØπ‰∫ÆÂ∫¶ (Relative Luminance)
// Âü∫‰∫é WCAG 2.0 ÂÖ¨Âºè: https://www.w3.org/TR/WCAG20/#relativeluminancedef
function getLuminance(r: number, g: number, b: number): number {
  const [rs, gs, bs] = [r, g, b].map(c => {
    c = c / 255;
    return c <= 0.03928 ? c / 12.92 : Math.pow((c + 0.055) / 1.055, 2.4);
  });
  return 0.2126 * rs + 0.7152 * gs + 0.0722 * bs;
}

// ËÆ°ÁÆóÂØπÊØîÂ∫¶ (Contrast Ratio)
// Âü∫‰∫é WCAG 2.0 ÂÖ¨Âºè: https://www.w3.org/TR/WCAG20/#contrast-ratiodef
function getContrastRatio(rgb1: { r: number; g: number; b: number }, rgb2: { r: number; g: number; b: number }): number {
  const lum1 = getLuminance(rgb1.r, rgb1.g, rgb1.b);
  const lum2 = getLuminance(rgb2.r, rgb2.g, rgb2.b);
  const lighter = Math.max(lum1, lum2);
  const darker = Math.min(lum1, lum2);
  return (lighter + 0.05) / (darker + 0.05);
}

// Âà§Êñ≠ËÉåÊôØËâ≤ÊòØÂê¶‰∏∫Ê∑±Ëâ≤
function isDarkBackground(bgColor: string): boolean {
  const rgb = hexToRgb(bgColor);
  if (!rgb) return false;
  
  const luminance = getLuminance(rgb.r, rgb.g, rgb.b);
  return luminance < 0.5; // ‰∫ÆÂ∫¶Â∞è‰∫é 0.5 Âà§ÂÆö‰∏∫Ê∑±Ëâ≤ËÉåÊôØ
}

// Ê†πÊçÆËÉåÊôØËâ≤Ëá™Âä®ÈÄâÊã©ÊúÄ‰Ω≥ÊñáÂ≠óÈ¢úËâ≤ÔºàÈªëËâ≤ÊàñÁôΩËâ≤Ôºâ
// Á°Æ‰øùÂØπÊØîÂ∫¶ >= 4.5:1 (WCAG AA Ê†áÂáÜ)
function getAdaptiveTextColor(bgColor: string): string {
  const rgb = hexToRgb(bgColor);
  if (!rgb) return '#000000';
  
  const whiteRgb = { r: 255, g: 255, b: 255 };
  const blackRgb = { r: 0, g: 0, b: 0 };
  
  const whiteContrast = getContrastRatio(rgb, whiteRgb);
  const blackContrast = getContrastRatio(rgb, blackRgb);
  
  // ÈÄâÊã©ÂØπÊØîÂ∫¶Êõ¥È´òÁöÑÈ¢úËâ≤
  return whiteContrast > blackContrast ? '#ffffff' : '#000000';
}

// Ê†πÊçÆËÉåÊôØËâ≤ÁîüÊàêÂçäÈÄèÊòéÁöÑËá™ÈÄÇÂ∫îÈ¢úËâ≤ÔºàÁî®‰∫éÊåâÈíÆÁ≠âÂÖÉÁ¥†Ôºâ
function getAdaptiveOverlayColor(bgColor: string, opacity: number = 0.2): string {
  const isDark = isDarkBackground(bgColor);
  const baseColor = isDark ? '255, 255, 255' : '0, 0, 0';
  return `rgba(${baseColor}, ${opacity})`;
}

// Ê†πÊçÆËÉåÊôØËâ≤ÁîüÊàêËæπÊ°ÜÈ¢úËâ≤
function getAdaptiveBorderColor(bgColor: string, opacity: number = 0.2): string {
  const isDark = isDarkBackground(bgColor);
  const baseColor = isDark ? '255, 255, 255' : '0, 0, 0';
  return `rgba(${baseColor}, ${opacity})`;
}

