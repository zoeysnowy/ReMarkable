            <span style={{ 
              color: adaptiveColors.textColor, // üé® Ëá™ÈÄÇÂ∫îÊñáÂ≠óÈ¢úËâ≤
              fontStyle: 'italic',
              opacity: 0.6
            }}>
              ÊöÇÊó†Ê†áÁ≠æ
            </span>
